package wu.MAIN;

public class Xinchen{
	public static void main(String[] args) {
		// %: modulo (mod)
		int n = 3;
		// if n modulo 2 is exactly 1
		if(n % 2 == 1) { // checks if n is odd
			System.out.println("n is odd");
		}
		else { // not odd, else it is even
			System.out.println("n is even");
		}
	}
}
